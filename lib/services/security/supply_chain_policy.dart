/// True when [license] (case-insensitive SPDX id) is OK to ship
/// inside the production bundle.
bool isLicenseAllowedInProd(String license) {
  final lower = license.toLowerCase();
  for (final p in SupplyChainPolicy.licenses) {
    if (!p.allowedInProdBundle) continue;
    for (final l in p.exampleLicenses) {
      if (l.toLowerCase() == lower) return true;
    }
  }
  return false;
}

/// True when [license] is on the explicit deny-list for prod (e.g.
/// GPL family). False when unknown — unknown means "needs legal
/// triage" rather than auto-deny.
bool isLicenseDeniedInProd(String license) {
  final lower = license.toLowerCase();
  for (final p in SupplyChainPolicy.licenses) {
    if (p.allowedInProdBundle) continue;
    for (final l in p.exampleLicenses) {
      if (l.toLowerCase() == lower) return true;
    }
  }
  return false;
}
